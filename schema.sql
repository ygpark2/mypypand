
CREATE TABLE IF NOT EXISTS `inhidb`.`profile` (
  id INT NOT NULL PRIMARY KEY,
  name VARCHAR(100),
  sex VARCHAR(1),
  age INT(2)
) ENGINE=InnoDB DEFAULT CHARSET=UTF8 ;

CREATE TABLE IF NOT EXISTS `inhidb`.`experiment` (
  FOREIGN KEY (profile) REFERENCES profile(id),
  Track INTEGER(1) NOT NULL,
  TestTime DECIMAL(10,10) NOT NULL,
  TripMeter DECIMAL(10,10) NOT NULL,
  Speed DECIMAL(10,10) NOT NULL,
  SpeedLimit DECIMAL(10,10) NOT NULL,
  AccelPedal DECIMAL(10,10) NOT NULL,
  BrakePedal DECIMAL(10,10) NOT NULL,
  SteeringWheel DECIMAL(10,10) NOT NULL,
  LaneLateralShift DECIMAL(10,10) NOT NULL,
  RoadCurvature DECIMAL(10,10) NOT NULL,
  CarX DECIMAL(10,10) NOT NULL,
  CarY DECIMAL(10,10) NOT NULL,
  CarAccelX DECIMAL(10,10) NOT NULL,
  CarAccelY DECIMAL(10,10) NOT NULL,
  FacelabTime DECIMAL(10,10) NOT NULL,
  FacelabObject DECIMAL(10,10) NOT NULL,
  FacelabID DECIMAL(10,10) NOT NULL,
  FacelabDist2Object DECIMAL(10,10) NOT NULL,
  Crash DECIMAL(10,10) NOT NULL,
  Location DECIMAL(10,10) NOT NULL,
  Distance2RLX DECIMAL(10,10) NOT NULL,
  DistTrain2RLX DECIMAL(10,10) NOT NULL,
  Time2RLX DECIMAL(10,10) NOT NULL,
  TimeTrain2RLX DECIMAL(10,10) NOT NULL,
  Message2send2phone DECIMAL(10,10) NOT NULL,
  Conso DECIMAL(10,10) NOT NULL,
  TrainActivated DECIMAL(10,10) NOT NULL,
  TrainSpeedOblig DECIMAL(10,10) NOT NULL,
  WarningActivated DECIMAL(10,10) NOT NULL,
  CarAroundCrossing DECIMAL(10,10) NOT NULL,
  TrainFrontX DECIMAL(10,10) NOT NULL,
  TrainFrontY DECIMAL(10,10) NOT NULL,
  TrainBackX DECIMAL(10,10) NOT NULL,
  TrainBackY DECIMAL(10,10) NOT NULL,
  RPM DECIMAL(10,10) NOT NULL,
  TTC DECIMAL(10,10) NOT NULL,
  Gear DECIMAL(10,10) NOT NULL,
) ENGINE=InnoDB DEFAULT CHARSET=UTF8;
